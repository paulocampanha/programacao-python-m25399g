# Nesse programa vamos estudar tipod de dados em Python

# int (inteiro) armaneza números inteiro
idade = 15
numero_de_alunos = 40
print('Idade:', idade)
print('Número de alunos:', numero_de_alunos )

# float (ponto flutuante) armazena números decimais.
altura = 1.75
preco = 59.99
print('Altura:', altura)
print("Preço: R$",preco)

# str (String) armazena sequências de caracteres.
nome = 'Gaspar Rodrigues'
frase = 'Amanhã é Domingo. Acordarei as 11 horas.'
print('Nome:', nome)
print('Frase:', frase)

# bool (Boleano) armazena apenas dois valores: 
# True (verdadeiro) ou False (false)
esta_chovendo = False
tem_cafe = True 
print('Está chovendo:', esta_chovendo)
print('Tem café:', tem_cafe)

# Constantes: constantes são variáveis imutáveis, ou seja, não 
# alteram seu valor durante a execução do programa
# Obs.: O PYTHON NÃO TEM CONSTANTES DE FATO.
PI = 3.14159
TAXA_DE_JUROS = 0.05


