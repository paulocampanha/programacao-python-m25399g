# Nesse programa vamos estudar os operadores aritméticos do Python

x = 4
y = 23
# Soma
soma = x + y
print("x: ",x)
print('y: ', y)
print('Soma:', soma)
# Subtração
sub = x - y
print("Subtração: ", sub)
# Multiplicação
mult = x * y
print('Multiplicação:', mult)
# Divisão
# Na divisão o Python sempre retorno o resultado como float
div = x / y
print("Divisão:", div)
# Divisão inteira
# Na divisão inteira o Python retorna um numero inteiro e 
# exclui a parte decimal
div_inteira = x // y
print("Divisão inteira:", div_inteira)
# Modulo (resto da divisão)
# Usamos o operador '%' para retornar o resto de uma divisão
modulo = x % y
print("Resto:", modulo)
# Potência
potencia = x ** y
print("Potencia:", potencia)
